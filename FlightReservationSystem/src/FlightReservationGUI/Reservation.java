    public void writeToFile(){
        String dosyaYolu = "src\\reservations.txt"; // Yaz覺lacak dosyan覺n yolu ve ad覺
        
        try {
            FileWriter dosyaYazici = new FileWriter(dosyaYolu,true);
            PrintWriter yaz = new PrintWriter(dosyaYazici);
            yaz.println(boardingComboBox.getSelectedItem()+" "+destinationComboBox.getSelectedItem()+" "+dateComboBox.getSelectedItem()+" "+timeComboBox.getSelectedItem());
            yaz.close();
          
        } catch (IOException e) {
            System.err.println("Dosya yazma hatas覺: " + e.getMessage());
        }
    }
