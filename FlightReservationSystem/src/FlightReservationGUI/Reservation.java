    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         if ((String) boardingComboBox.getSelectedItem()==(String) destinationComboBox.getSelectedItem()){
             JOptionPane.showMessageDialog(null, "Boarding and Destination is the same !!!","ERROR!",JOptionPane.ERROR_MESSAGE);
         }
        
        if((String) boardingComboBox.getSelectedItem()=="Izmir"&&(String) destinationComboBox.getSelectedItem()=="Ankara"||
                 (String) boardingComboBox.getSelectedItem()=="Ankara"&&(String) destinationComboBox.getSelectedItem()=="Izmir"){
            priceTextArea.setText("");
        priceTextArea.append("1399.99 TL");
        
         }
         if((String) boardingComboBox.getSelectedItem()=="Izmir"&&(String) destinationComboBox.getSelectedItem()=="Istanbul"||
                 (String) boardingComboBox.getSelectedItem()=="Istanbul"&&(String) destinationComboBox.getSelectedItem()=="Izmir"){
              priceTextArea.setText("");
        priceTextArea.append("1249.99 TL");
        
         }
         if((String) boardingComboBox.getSelectedItem()=="Izmir"&&(String) destinationComboBox.getSelectedItem()=="Antalya"||
                 (String) boardingComboBox.getSelectedItem()=="Antalya"&&(String) destinationComboBox.getSelectedItem()=="Izmir"){
              priceTextArea.setText("");
        priceTextArea.append("1199.99 TL");
        
         }
         if((String) boardingComboBox.getSelectedItem()=="Ankara"&&(String) destinationComboBox.getSelectedItem()=="Istanbul"||
                 (String) boardingComboBox.getSelectedItem()=="Istanbul"&&(String) destinationComboBox.getSelectedItem()=="Ankara"){
              priceTextArea.setText("");
        priceTextArea.append("1399.99 TL");
        
         }
         if((String) boardingComboBox.getSelectedItem()=="Antalya"&&(String) destinationComboBox.getSelectedItem()=="Istanbul"||
                 (String) boardingComboBox.getSelectedItem()=="Istanbul"&&(String) destinationComboBox.getSelectedItem()=="Antalya"){
              priceTextArea.setText("");
        priceTextArea.append("1749.99 TL");
        
         }
         if((String) boardingComboBox.getSelectedItem()=="Antalya"&&(String) destinationComboBox.getSelectedItem()=="Ankara"||
                 (String) boardingComboBox.getSelectedItem()=="Ankara"&&(String) destinationComboBox.getSelectedItem()=="Antalya"){
              priceTextArea.setText("");
        priceTextArea.append("1049.99 TL");
       
         }
    }//GEN-LAST:event_jButton2ActionPerformed

